-- UPSERT voci in catalog_produttore (code calcolato da label)
INSERT INTO catalog_produttore (code, label, sort_order, is_active)
SELECT
  CONCAT('PRD_', UPPER(SUBSTRING(MD5(TRIM(label)), 1, 16))) AS code,
  label,
  sort_order,
  1 AS is_active
FROM tmp_catalog_produttore
ON DUPLICATE KEY UPDATE
  label = VALUES(label),
  sort_order = VALUES(sort_order),
  is_active = 1,
  updated_at = CURRENT_TIMESTAMP;

-- Soft disable: tutto cio' che non e' presente nella lista ufficiale
UPDATE catalog_produttore cp
LEFT JOIN tmp_catalog_produttore t ON t.label = cp.label
SET cp.is_active = 0,
    cp.updated_at = CURRENT_TIMESTAMP
WHERE t.label IS NULL;

